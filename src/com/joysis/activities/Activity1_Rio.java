package com.joysis.activities;

public class Activity1_Rio {
    public static void main(String[] args) {
        // declare variables
        boolean myBool = true;
        byte myByte = 127; 
        short myShort = 32000; 
        int myInt = 1234567890; 
        long myLong = 123456789012345L; 
        float myFloat = 3.14f; 
        double myDouble = 3.14159; 
        char myChar = 'A'; 
        String myString = "Hello, Java!"; 
    
        // print values with appropriate labels
        System.out.println("Boolean value: " + myBool);
        System.out.println("Byte value: " + myByte);
        System.out.println("Short value: " + myShort);
        System.out.println("Int value: " + myInt);
        System.out.println("Long value: " + myLong);
        System.out.println("Float value: " + myFloat);
        System.out.println("Double value: " + myDouble);
        System.out.println("Char value: " + myChar);
        System.out.println("String value: " + myString);
    }
}
